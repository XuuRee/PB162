package cz.muni.fi.pb162.project.geometry;

/**
 * @author xvalchar
 */
public class Vertex2D {
    private double x;
    private double y;

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double newX) {
        this.x = newX;
    }

    public void setY(double newY) {
        this.y = newY;
    }

    /**
     * @return String location of point
     */
    public String toString() {
        return("[" + x + ", " + y + "]");
    }
}

